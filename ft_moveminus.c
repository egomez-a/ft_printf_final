#include "ft_printf.h"

char	*ft_moveminus(char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if ((s[i] == '-') && (s[(i + 1)] > 0))
		{
			s[i] = '0';
			s[0] = '-';
		}
		i++;
	}
	return (s);
}
